FK_PrgItemTypeAttribute#ItemTypes FOREIGN KEY (PrgItemTypeAttributeID) 
REFERENCES dbo.PrgItemTypeAttribute (ID) 
	ON UPDATE  NO ACTION
	ON DELETE  CASCADE

